public class Switch {
    public static void main(String[] args){
        int day = 4;

        switch(day){
            case 6:
            System.out.println("Today is Saturday");
            break;
            case 2:
            System.out.println("Today is Tuesday");
            break;
            default:
            System.out.println("This is a default print");
        }
    }
}
